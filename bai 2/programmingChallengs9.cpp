#include <iostream>
using namespace std;

int main() 
{
    cout << "The size of char is: " << sizeof(char) << " byte(s)" << endl;
    cout << "The size of int is: " << sizeof(int) << " byte(s)" << endl;
    cout << "The size of float is: " << sizeof(float) << " byte(s)" << endl;
    cout << "The size of double is: " << sizeof(double) << " byte(s)" << endl;

    return 0;
}
