#include <iostream>
using namespace std;

int main()
{
    double a = 28.0;  // Giá trị a
    double b = 32.0;  // Giá trị b
    double c = 37.0;  // Giá trị c
    double d = 24.0;  // Giá trị d
    double e = 33.0;  // Giá trị e

    double sum = a + b + c + d + e;  // Tính tổng của các giá trị
    double average = sum / 5.0;  // Tính trung bình

    cout << "trung binh cua cac gia tri la: " << average << endl;

    return 0;
}
